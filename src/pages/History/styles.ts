// Fazendo o component de status receber propriedades

// Mapeando as cores
const STATUS_COLORS = {
  yellow: 'yellow-500',
  green: 'green-500',
  red: 'red-500',
} as const

// o "as const" serve para definir para o typescript que as variáveis devem ser aquelas e não strings

interface StatusProps {
  // statusColor: 'yellow' | 'red' | 'green'

  // Não é possível passar o STATUS_COLORS diretamente, é preciso passar keyof typeof
  statusColor: keyof typeof STATUS_COLORS
}

export const Status = styled.span<StatusProps>`
  display: flex;
  align-items: center;
  gap: 0.5rem;

  /* Adicionando um elemento novo com o css */
  &::before {
    content: '';
    width: 0.5rem;
    height: 0.5rem;
    border-radius: 9999px;
    background: ${(props) => props.theme[STATUS_COLORS[props.statusColor]]};
  }
`
